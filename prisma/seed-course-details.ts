async function main() {
    console.log("ðŸŒ± Seeding course instructors, FAQs, requirements & outcomes...\n");

    for (const extras of courseExtras) {
        const course = await prisma.course.findUnique({
            where: { slug: extras.slug },
        });

        if (!course) {
            console.log(`âš ï¸  Course "${extras.slug}" not found in DB â€” skipping`);
            continue;
        }

        // Update requirements & outcomes arrays
        await prisma.course.update({
            where: { id: course.id },
            data: {
                requirements: extras.requirements,
                outcomes: extras.outcomes,
            },
        });

        // Delete existing instructors & FAQs (idempotent)
        await prisma.courseInstructor.deleteMany({ where: { courseId: course.id } });
        await prisma.courseFAQ.deleteMany({ where: { courseId: course.id } });

        // Create instructors
        if (extras.instructors.length > 0) {
            await prisma.courseInstructor.createMany({
                data: extras.instructors.map((inst, i) => ({
                    courseId: course.id,
                    name: inst.name,
                    role: inst.role,
                    bio: inst.bio,
                    initials: inst.initials,
                    sortOrder: i,
                })),
            });
        }

        // Create FAQs
        if (extras.faqs.length > 0) {
            await prisma.courseFAQ.createMany({
                data: extras.faqs.map((faq, i) => ({
                    courseId: course.id,
                    question: faq.question,
                    answer: faq.answer,
                    sortOrder: i,
                })),
            });
        }

        console.log(
            `âœ… ${extras.slug}: ${extras.instructors.length} instructors, ${extras.faqs.length} FAQs, ${extras.requirements.length} requirements, ${extras.outcomes.length} outcomes`
        );
    }

    console.log("\nðŸŽ‰ Done!");
}

